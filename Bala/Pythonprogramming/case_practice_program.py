### Upper case ###
a="this is my practice program"
print(a.upper())

### Lower case ###
b="THIS IS MY PRACTICE PROGRAM"
print(b.lower())

### capitalize ###
c="testing program"
print(c.capitalize())

### Title (Auto caps) ###
d="auto caps check"
print(d.title())

### Swap case ###
e="caps and small case CHECKING"
print(e.swapcase())

### Strip ####
f="   hello everyone   "
print(f.strip())